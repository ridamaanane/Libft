#include "libft.h"

int ft_str_is_print(char *str)
{
    int i = 0;
    while (str[i] != '\0')
    {
        if (str[i] >= 32 && str[i] <= 126)
            i++;
        else
            return (0);
        i++;
    }
    return (1);
}